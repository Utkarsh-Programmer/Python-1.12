# `type` function is used to get the data type of a given variable.
a = 31
print(type(a))  # class <int>

b = "Harry"
print(type(b))  # class <str>


# Typecasting...
print(str(31))  # integer to string
print(int("32"))  # string to integer
print(float(32))  # integer to float

# NOTE: string with letters can't typecast.
