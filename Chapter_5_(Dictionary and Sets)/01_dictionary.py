# NOTE: dictionaries are unordered, indexed and mutable.
# NOTE: dictionaries are key-value pairs.
# NOTE: dictionaries cannot contain duplicate keys.

marks = {
    "Harry": 100,
    "Shubham": 56,
    "Rohan": 23
}
print(marks)
print(type(marks))  # <class 'dict'>

print(marks["Harry"])  # prints `100`
