# `\n` can be used to make a new line.
a = "Harry is a good boy\nbut not a bad boy."
print(a)

# `\t` can be used to make a tab space.
b = "\thello"
print(b)

# `\"` can be used to use double quotes inside double quotes.
c = "In \"double quotes\""
print(c)

# `\'` can be used to use single quotes inside single quotes.
d = 'In \'double quotes\''
print(d)

# `\\` can be used to use backslash.
e = "this is a backslash \\"
print(e)

