# `len` function...
# used to find the length of an iterable.
name = "harry"
print(len(name))

# `endswith` method...
# used to check whether a string ends with the specified character.
print(name.endswith("ry"))

# `startswith` method...
# used to check whether a string starts with the specified character.
print(name.startswith("H"))

# `capitalize` method
# used to capitalize the first letter of a string.
print(name.capitalize())

